//==============================================================================
void App::OnCleanup() {
	Area::AreaControl.OnCleanup();

    //--------------------------------------------------------------------------
    // Entities
    //--------------------------------------------------------------------------
    for(int i = 0;i < Entity::EntityList.size();i++) {
        if(!Entity::EntityList[i]) continue;

        Entity::EntityList[i]->OnCleanup();
    }

    Entity::EntityList.clear();

    SDL_FreeSurface(Surf_Display);
    SDL_Quit();
}

