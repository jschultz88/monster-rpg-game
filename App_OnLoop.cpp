#include "App.h"

//==============================================================================
void App::OnLoop() {
    //--------------------------------------------------------------------------
    // Entities
    //--------------------------------------------------------------------------
    for(int i = 0;i < Entity::EntityList.size();i++) {
        if(!Entity::EntityList[i]) continue;

        Entity::EntityList[i]->OnLoop();
    }

    FPS::FPSControl.OnLoop();

    char Buffer[255];
    sprintf(Buffer, "%d", FPS::FPSControl.GetFPS());
    SDL_WM_SetCaption(Buffer, Buffer);
}

